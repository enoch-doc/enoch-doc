var date = new Date()
var h = date.getHours()
var min =  date.getMinutes().toString()
// var sec =  date.getSeconds()
function show() {
    if ((h<10)) {
        document.getElementById("date").innerHTML = `0${h} ${min}`   
    } else if (min<10) {
        document.getElementById("date").innerHTML = `${h} 0${min}`
    } else if(h>10 && min<10){
        document.getElementById("date").innerHTML = `${h} ${min}`
    } else if (h >10 || min >10){
        document.getElementById("date").innerHTML = `${h} ${min}`
    }
    
}